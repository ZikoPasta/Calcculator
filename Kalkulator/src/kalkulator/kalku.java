    private void sdenganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sdenganActionPerformed
        ang2 = Double.parseDouble(angka);
        switch (op) {
            case 1 :
            hasil = ang1 + ang2;
            angka = Double.toString(hasil);
            tampilan.setText(angka);
            break;

            case 2 :
            hasil = ang1 - ang2;
            angka = Double.toString(hasil);
            tampilan.setText(angka);
            break;

            case 3 :
            hasil = ang1 * ang2;
            angka = Double.toString(hasil);
            tampilan.setText(angka);
            break;

            case 4 :
            hasil = ang1 / ang2;
            angka = Double.toString(hasil);
            tampilan.setText(angka);
            break;
        }
    }
